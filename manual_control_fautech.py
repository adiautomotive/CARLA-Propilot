def _get_angle_to_waypoint(self,world, waypoint):
    transform = world.player.get_transform()
    car_loc = transform.location
    car_yaw = math.radians(transform.rotation.yaw)
    forward_vector = np.array([math.cos(car_yaw), math.sin(car_yaw)])
    target_loc = waypoint.transform.location
    dir_vector = np.array([target_loc.x - car_loc.x, target_loc.y - car_loc.y])
    dir_vector = dir_vector / np.linalg.norm(dir_vector)
    dot = np.dot(forward_vector, dir_vector)
    det = forward_vector[0]*dir_vector[1] - forward_vector[1]*dir_vector[0]
    angle = math.degrees(math.atan2(det, dot))
    return angle

def _get_speed(self, world):
    velocity = world.player.get_velocity()
    return math.sqrt(velocity.x**2 + velocity.y**2 + velocity.z**2) * 3.6  # km/h

